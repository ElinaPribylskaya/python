"""Задание №1. Задача №1. Ввести три числа m, n, p. Подсчитать количество отрицательных чисел."""

m = int(input("Введите число m: "))
n = int(input("Введите число n: "))
p = int(input("Введите число p: "))

count = 0
if m < 0: count += 1
if n < 0: count += 1
if p < 0: count += 1

print("Количество отрицательных чисел среди m, n, p: ", count)

